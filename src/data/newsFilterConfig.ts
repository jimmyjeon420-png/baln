/**
 * newsFilterConfig — 뉴스 키워드 필터 설정
 *
 * 역할: 투자자에게 관련 없는 뉴스를 제목/요약 기반으로 걸러냄
 * 비유: 카페 입구에서 "오늘의 메뉴와 무관한 전단지"를 걸러내는 필터
 *
 * 사용처:
 * - src/hooks/useMarketNews.ts (뉴스 조회 후 필터링)
 */

// =============================================================================
// 차단 키워드 (제목 or 요약에 포함되면 제외)
// =============================================================================

/**
 * 복합 키워드: 이 문구가 정확히 포함되면 차단
 * "인사" 단독은 오탐 위험 → "인사발령", "과장급 인사" 등 복합어로 등록
 */
export const BLOCKED_PHRASES: string[] = [
  // ── 인사·인사이동 ──
  '인사발령',
  '과장급 인사',
  '부장급 인사',
  '국장급 인사',
  '국장급 전보',
  '부이사관 인사',
  '서기관 인사',
  '사무관 인사',
  '승진자 명단',
  '인사이동',
  '전보 발령',
  '보직 변경',
  '직위 해제',
  '정기 인사',
  '특별 인사',
  '임원 인사',

  // ── 정치·행정 (투자 무관) ──
  '국회의원 선거',
  '당대표 경선',
  '탄핵 소추',
  '정당 해산',
  '도의회 의원',
  '시의회 의원',

  // ── 연예·스포츠 ──
  '아이돌 컴백',
  '드라마 시청률',
  '예능 프로그램',
  '프로야구 경기',
  '축구 경기 결과',

  // ── 사건사고 (투자 무관) ──
  '교통사고 발생',
  '음주운전 적발',
  '실종자 수색',
];

/**
 * 단일 키워드: 제목에 이 단어만 포함되어도 차단
 * 투자 맥락에서 절대 쓰이지 않는 단어만 등록
 */
export const BLOCKED_SINGLE_KEYWORDS: string[] = [
  '부고',
  '조문',
  '장례',
];

// =============================================================================
// 필터 함수
// =============================================================================

/**
 * 뉴스 아이템이 차단 키워드에 해당하는지 판별
 * @returns true면 차단 (표시하지 않음)
 */
export function isBlockedNews(title: string, summary?: string | null): boolean {
  const text = `${title} ${summary ?? ''}`;

  // 복합 키워드 체크
  for (const phrase of BLOCKED_PHRASES) {
    if (text.includes(phrase)) return true;
  }

  // 단일 키워드 체크 (제목만)
  for (const kw of BLOCKED_SINGLE_KEYWORDS) {
    if (title.includes(kw)) return true;
  }

  return false;
}
