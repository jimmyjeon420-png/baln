/**
 * heartAsset.ts - 하트(관심) 자산 타입 정의
 *
 * 역할: "관심 자산 명부 양식"
 * - 가격/수량 없이 이름과 티커만 저장
 * - AsyncStorage에 저장되는 형식 정의
 * - 건강 신호등 포함 UI 타입
 *
 * Anti-Toss 원칙:
 * - Heart/Like: 매수가/수량 입력 없음, ❤️로 관심 표시만
 * - 보험 BM: 건강 점수는 무료, 상세 분석은 프리미엄
 */

// ============================================================================
// 기본 타입
// ============================================================================

/** 자산 종류 (하트 자산용) */
export type HeartAssetType = 'stock' | 'crypto' | 'etf';

/** 하트 자산 (관심 등록된 자산) */
export interface HeartAsset {
  /** 자산 이름 (표시용) */
  name: string; // '삼성전자', '비트코인', '애플'

  /** 티커 심볼 */
  ticker: string; // '005930', 'BTC', 'AAPL'

  /** 자산 종류 */
  type: HeartAssetType;

  /** 하트 등록 시각 (ISO 8601) */
  heartedAt: string; // '2026-02-09T14:30:00.000Z'
}

// ============================================================================
// UI 표시용 타입
// ============================================================================

/** 하트 자산 + 건강 신호 (HealthSignalCard에서 사용) */
export interface HeartAssetWithSignal extends HeartAsset {
  /** 건강 신호등 */
  signal: 'green' | 'yellow' | 'red';

  /** 신호 사유 (프리미엄 전용, 선택적) */
  signalReason?: string;
}

// ============================================================================
// 저장 형식
// ============================================================================

/**
 * AsyncStorage 저장 형식
 * 키: '@baln:heart_assets'
 * 값: JSON.stringify(HeartAsset[])
 */
export type HeartAssetsStorage = HeartAsset[];
